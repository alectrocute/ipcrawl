// One-shot script to regenerate `app/assets/world-path.ts`.
//
// Uses d3-geo's equirectangular projection (which properly clips at the
// antimeridian, so polygons that cross ±180° don't span the entire map and
// produce inverted-fill artifacts) against world-atlas's land-110m TopoJSON.
//
// Usage: node scripts/generate-world-path.mjs
//
// Pull the TopoJSON first:
//   curl -s https://cdn.jsdelivr.net/npm/world-atlas@2/land-110m.json -o /tmp/land-110m.json
//
// And install transient deps:
//   npm install --no-save d3-geo topojson-client

import { feature } from 'topojson-client'
import { geoEquirectangular, geoPath } from 'd3-geo'
import { readFileSync, writeFileSync } from 'node:fs'

const SRC = '/tmp/land-110m.json'
const OUT = 'app/assets/world-path.ts'

// We want the world to fit a 1000x500 SVG viewport (equirectangular's natural
// 2:1 aspect ratio). d3-geo's equirectangular has a default scale of 152.87.
// A scale of 1000/(2π) ≈ 159.155 maps a full longitude/latitude span exactly
// to the requested width.
const WIDTH = 1000
const HEIGHT = 500
const SCALE = WIDTH / (2 * Math.PI)

const topo = JSON.parse(readFileSync(SRC, 'utf8'))
const land = feature(topo, topo.objects.land)

const projection = geoEquirectangular()
  .scale(SCALE)
  .translate([WIDTH / 2, HEIGHT / 2])

const path = geoPath().projection(projection)
const d = path(land) || ''

const header = `// Generated by scripts/generate-world-path.mjs from world-atlas land-110m.json\n`
  + `// in equirectangular projection (${WIDTH}x${HEIGHT}). Antimeridian-safe.\n`
  + `// Regenerate when the source data changes.\n`

writeFileSync(OUT, `${header}export const WORLD_PATH = ${JSON.stringify(d)}\n`)
console.log(`wrote ${OUT}  (${d.length} chars)`)
